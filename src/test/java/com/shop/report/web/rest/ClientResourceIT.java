    @Test
    @Transactional
    void putNewClient() throws Exception {
        // Initialize the database
        clientRepository.saveAndFlush(client);

        int databaseSizeBeforeUpdate = clientRepository.findAll().size();

        // Update the client
        Client updatedClient = clientRepository.findById(client.getId()).get();
        // Disconnect from session so that the updates on updatedClient are not directly saved in db
        em.detach(updatedClient);
        updatedClient
            .name(UPDATED_NAME)
            .surName(UPDATED_SUR_NAME)
            .debtAmount(UPDATED_DEBT_AMOUNT)
            .address(UPDATED_ADDRESS)
            .phone(UPDATED_PHONE)
            .notes(UPDATED_NOTES)
            .createdDate(UPDATED_CREATED_DATE)
            .code(UPDATED_CODE);
        ClientDTO clientDTO = clientMapper.toDto(updatedClient);

        restClientMockMvc
            .perform(
                put(ENTITY_API_URL_ID, clientDTO.getId())
                    .contentType(MediaType.APPLICATION_JSON)
                    .content(TestUtil.convertObjectToJsonBytes(clientDTO))
            )
            .andExpect(status().isOk());

        // Validate the Client in the database
        List<Client> clientList = clientRepository.findAll();
        assertThat(clientList).hasSize(databaseSizeBeforeUpdate);
        Client testClient = clientList.get(clientList.size() - 1);
        assertThat(testClient.getName()).isEqualTo(UPDATED_NAME);
        assertThat(testClient.getSurName()).isEqualTo(UPDATED_SUR_NAME);
        assertThat(testClient.getDebtAmount()).isEqualTo(UPDATED_DEBT_AMOUNT);
        assertThat(testClient.getAddress()).isEqualTo(UPDATED_ADDRESS);
        assertThat(testClient.getPhone()).isEqualTo(UPDATED_PHONE);
        assertThat(testClient.getNotes()).isEqualTo(UPDATED_NOTES);
        assertThat(testClient.getCreatedDate()).isEqualTo(UPDATED_CREATED_DATE);
        assertThat(testClient.getCode()).isEqualTo(UPDATED_CODE);
    }

