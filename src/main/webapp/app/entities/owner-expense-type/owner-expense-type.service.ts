import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IOwnerExpenseType } from '@/shared/model/owner-expense-type.model';

const baseApiUrl = 'api/owner-expense-types';

export default class OwnerExpenseTypeService {
  public find(id: number): Promise<IOwnerExpenseType> {
    return new Promise<IOwnerExpenseType>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IOwnerExpenseType): Promise<IOwnerExpenseType> {
    return new Promise<IOwnerExpenseType>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IOwnerExpenseType): Promise<IOwnerExpenseType> {
    return new Promise<IOwnerExpenseType>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IOwnerExpenseType): Promise<IOwnerExpenseType> {
    return new Promise<IOwnerExpenseType>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
