import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { IBorrowedDebtType } from '@/shared/model/borrowed-debt-type.model';

const baseApiUrl = 'api/borrowed-debt-types';

export default class BorrowedDebtTypeService {
  public find(id: number): Promise<IBorrowedDebtType> {
    return new Promise<IBorrowedDebtType>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(baseApiUrl + `?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IBorrowedDebtType): Promise<IBorrowedDebtType> {
    return new Promise<IBorrowedDebtType>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IBorrowedDebtType): Promise<IBorrowedDebtType> {
    return new Promise<IBorrowedDebtType>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IBorrowedDebtType): Promise<IBorrowedDebtType> {
    return new Promise<IBorrowedDebtType>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
